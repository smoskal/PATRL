	def split_data_set(self, sig_dict, test_split=.2, random_seed=None) :
	
		sig_list = []
		for sig, label in  sig_dict.items() :
		
			if isinstance(label, Enum) :
				sig_list.append([sig, label.value])
			else :
				sig_list.append([sig, label])
			
		training, testing = train_test_split(sig_list, test_size=test_split, random_state=random_seed)
		
		train_out = {}
		for sample in training:
			train_out[sample[0]] = sample[1]
			
		test_out = {}
		for sample in testing:
			test_out[sample[0]] = sample[1]	
		
		return train_out, test_out
		
		
